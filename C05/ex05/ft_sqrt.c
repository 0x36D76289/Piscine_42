int	ft_sqrt(int nb)
{
	long	i;
	long	tmp;

	i = 1;
	tmp = nb;
	if (tmp < 1)
		return (0);
	while (i * i < tmp)
		i++;
	if (i * i == tmp)
		return (i);
	return (0);
}
