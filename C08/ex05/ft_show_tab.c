#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char	*str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_putnbr(int nb)
{
	unsigned long	new;

	new = nb;
	if (new == 2147483647)
	{
		write(1, "2147483647", 10);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		new = -new;
	}
	if (new / 10 > 0)
		ft_putnbr(new / 10);
	ft_putchar((new % 10) + '0');
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		ft_putchar('\n');
		ft_putnbr(par[i].size);
		ft_putchar('\n');
		ft_putstr(par[i].copy);
		ft_putchar('\n');
		i++;
	}
}
