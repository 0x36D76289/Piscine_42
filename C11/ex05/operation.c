int	multiple(int a, int b)
{
	return (a * b);
}

int	plus(int a, int b)
{
	return (a + b);
}

int	moins(int a, int b)
{
	return (a - b);
}

int	modulo(int a, int b)
{
	return (a % b);
}

int	diviser(int a, int b)
{
	return (a / b);
}
